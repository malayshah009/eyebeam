    <h4 class="my-3">WHY WEARING SUNGLASSES IN WINTER IS IMPORTANT</h4>
    <p class="mb-4">Eye-health experts contend that wearing sunglasses during the winter is more crucial than doing so during the summer months.
        Why? During the winter, eyes are exposed to the sun’s damaging UV rays, and the rays reflected back by the mirror-like snow.
        Sunglasses worn from November through March can help reduce winter glare, snow blindness, and vision-related headaches.
    </p>
    <h4 class="my-3">UV PROTECTION AND POLARIZED LENSES</h4>
    <p>The most harmful ultraviolet rays are UV-B rays; these can cause DNA damage and may increase the risk of developing cancer.
        High-quality sunglasses will offer protection against all types of ultraviolet rays, including UV-B rays.
        Without any protection at all, prolonged exposure to winter glare can result in short and long-term ocular problems,
        such as snow blindness, cataracts, and myriad forms of eye cancers.
    </p>
    <p>Check to ensure winter sunglasses screen for more than 99% of UV-A and UV-B light and wavelengths up to or exceeding 400
        nano-meters. To further the fight against glare and long-term ocular problems like cataracts consider investing in 
        polarized sun-glass lenses. Polarized lenses are especially important for summer water sports and winter skiing–virtually
        any conditions in which high glare is encountered call for polarized lenses.
    </p>
    <p class="mb-4">In essence, when light reflects off the snow, the light waves are aligning in horizontal patterns and causing glare.
        Polarized lenses work by precluding these horizontal lines from reaching eyesight. This reduces glare, the chances of
        distraction, and eye strain. Also, with high-quality polarized lenses, the risks of developing skin damage and 
        premature aging (“crow’s feet”) around the eyes diminishes to the degree that the lenses are UV-protecting.
    </p>
    <h4 class="my-3">WORKING WITH NATURE: REDUCING EYESTRAIN</h4>
    <p class="mb-4"> Normally when people are exposed to high-light conditions, the pupil contracts so that only a small amount of light hits 
        the retina. In low-light conditions, the pupil dilates to allow more light to reach the retina. Nature’s solution sans 
        sunglasses is having people squint; unfortunately, this can result in eyestrain and headaches. Wearing sunglasses reduces 
        glare, prevents UV damage, improves comfort, and prevents eyestrain and headaches brought about by squinting.
    </p>
